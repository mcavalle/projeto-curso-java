package aula2;

public class OperadoresAritmeticos {
    public static void main(String[] args){
        int a = 10;
        int b = 3;
        int resultado; //Variavel que vai receber os valores resultantes das operacoes

        resultado = a + b;
        System.out.println(resultado);

        resultado = a - b;
        System.out.println(resultado);

        resultado = a * b;
        System.out.println(resultado);

        resultado = a / b;
        System.out.println(resultado);

        resultado = a % b;
        System.out.println(resultado);

    }
}
